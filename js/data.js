    if (indicators.복지 >= 4 && indicators["시민 반응"] >= 2 && welfarePolicies.length >= 4) {
      specialEndings.push({ type: "복지_천국", ...SPECIAL_ENDINGS["복지_천국"] });
    }
    
    // 재건의 설계자
